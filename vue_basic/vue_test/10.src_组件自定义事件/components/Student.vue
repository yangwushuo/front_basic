<template>
    <div class="box">
        <h2>学生姓名:{{name}}</h2>
        <h2>学生性别:{{sex}}</h2>
        <h2>当前求和为:{{number}}</h2>
        <button @click="add">点我number++</button>
        <button @click="sendStudentName">发送学生姓名</button>
        <button @click="unbind">解绑atguigu事件</button>
        <button @click="death">销毁当前Student组件的实例(vc)</button>
    </div>
</template>

<script>
    export default {
        name: 'Student',
        data() {
            return {
                name: 'Jason',
                sex: '男',
                number: 0
            }
        },
        methods: {
            sendStudentName(){
                //调用组件内的atuguigu事件
                this.$emit('atguigu',this.name,1,2,3,4);
            },
            add(){
                console.log("add回调被调用了");
                this.number++;
            },
            unbind(){
                this.$off("atguigu"); //解绑一个自定义事件
                // this.$off(['atguigu','demo']); //解绑多个自定义事件
                // this.$off(); //解绑所有自定义事件
            },
            death(){
                this.$destroy(); //销毁了当前Student组件的实例，销毁后所有Student实例的自定义事件全都不奏效
            }
        },
    }
</script>

<style scope>
    .box{
        background-color: yellowgreen;
        opacity: 0.8;
        padding: 5px;
        margin-top: 30px;
    }
</style>